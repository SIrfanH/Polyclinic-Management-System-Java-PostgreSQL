    public void updateTable(){
        Connection con = postgresql.connectorSender();
        
        int column_count;
        
        try{
            
            Statement stmt = con.createStatement();
            String Komut = String.format("SELECT MUAYENE_NO, HASTA_AD, HASTA_SOYAD, MUAYENE_ACIKLAMASI FROM KAYITLI_HASTALAR kh, MUAYENELER m"
                    + " WHERE kh.HASTA_NO = m.HASTA_NO and m.hasta_no = %d and m.DOKTOR_NO = %d ", hastaNo, this.doktor.getKimlikNo());
            ResultSet rs = stmt.executeQuery(Komut);
            ResultSetMetaData rsMetaData = rs.getMetaData();
            column_count = rsMetaData.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while (rs.next()) {
                Vector v2 = new Vector();
                
                for(int a=1; a<=column_count; a++)
                {
                 
                    v2.add(rs.getString("MUAYENE_NO"));
                    v2.add(rs.getString("HASTA_AD"));             
                    v2.add(rs.getString("HASTA_SOYAD"));
                    v2.add(rs.getString("MUAYENE_ACIKLAMASI"));
            
                }
                df.addRow(v2);
            }
        } catch (SQLException e) {
            System.out.println(e);
            System.out.println("Exception");
        } 
    }

