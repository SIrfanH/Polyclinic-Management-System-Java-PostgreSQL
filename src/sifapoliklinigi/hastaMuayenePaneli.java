    private void kaydetButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaydetButonActionPerformed
        // TODO add your handling code here:
        Connection conn = postgresql.getInstance();
        String aciklama = aciklamaTextField.getText();
        String ucret = ucretTextField.getText();
        
        if(!ucret.equals("")){
            try {
                String query = String.format(
                        "INSERT INTO MUAYENELER VALUES"
                        + "(DEFAULT, %d, %d, %d, \'%s\', \'%s\', %d);",
                        hastaNo, doktor.getKimlikNo(), Integer.parseInt(ucret),
                        aciklama, (String) belgeComboBox.getSelectedItem(), 0
                );
                
                conn.createStatement().executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Başarı ile kaydedildi");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Hata");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Lütfen ücreti girin");
        }
        
        this.setVisible(false);
        hastaBilgisiSayfasi.updateTableWithArray();
        hastaBilgisiSayfasi.setVisible(true);
        
    }//GEN-LAST:event_kaydetButonActionPerformed

